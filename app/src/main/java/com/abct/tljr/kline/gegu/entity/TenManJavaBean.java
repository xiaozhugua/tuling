package com.abct.tljr.kline.gegu.entity;

public class TenManJavaBean {
	private String name;
	private String ranking;
	private String rate;
	private String type;
	private String ud;
	private String ud_rate;
	private String volume;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRanking() {
		return ranking;
	}

	public void setRanking(String ranking) {
		this.ranking = ranking;
	}

	public String getRate() {
		return rate;
	}

	public void setRate(String rate) {
		this.rate = rate;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUd() {
		return ud;
	}

	public void setUd(String ud) {
		this.ud = ud;
	}

	public String getUd_rate() {
		return ud_rate;
	}

	public void setUd_rate(String ud_rate) {
		this.ud_rate = ud_rate;
	}

	public String getVolume() {
		return volume;
	}

	public void setVolume(String volume) {
		this.volume = volume;
	}

}
